package com.cripisi.calendar;

import java.sql.Date;

/**
 *
 * @author deathman28
 */
public class FullCalendar {

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Date getStart() {
        return start;
    }

    public void setStart(Date start) {
        this.start = start;
    }

    public Date getEnd() {
        return end;
    }

    public void setEnd(Date end) {
        this.end = end;
    }
    private int id;
private String title;
private Date start;
private Date end;
}
